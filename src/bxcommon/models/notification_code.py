    QUOTA_DEPLETED = 4400
    QUOTA_DEPLETED_TX_BLOCKED = 4401
    QUOTA_DEPLETED_TX_HANDLED_AS_FREE = 4402
    QUOTA_NOT_SET_TX_HANDLED_AS_FREE = 4403

    QUOTA_DEPLETED_BLOCK = 4500
    QUOTA_DEPLETED_BLOCK_BLOCKED = 4501
